                //cppLines.Add("map.insert(std::pair<int, int>(" + tes4FormID + ", " + tes5GlobalVariableFormID + "));");
                return new { TES4FormID = tes4FormID, GlobalVariableName = split[2], TES5FormID = tes5GlobalVariableFormID };
            }).ToDictionary(x => x.TES4FormID, x => new Tuple<int, string>(x.TES5FormID, x.GlobalVariableName));
            //string cppString = string.Join("\r\n", cppLines);//The cpp lines are only used to generate code for GECKFrontend.
        }

        public bool TryGetGlobalVariable(int tes4FormID, out Tuple<int, string> globalVariable)
